ORG 00
	MOV R3,#5
	ACALL FACTORIAL
	MOV 40H,A
	MOV R0,#41H
	ACALL HEX2DEC
	SJMP $

FACTORIAL:
	MOV A,#1
	NEXT: MOV B,R3
	MUL AB
	DJNZ R3,NEXT
	RET

	HEX2DEC:
	;CONVERTS HEX NUMBER IN A TO DECIMAL
	;PUTS THE RESULT IN ADDRESS POINTED BY R0
	MOV B,#10
	DIV AB
	MOV @R0,B
	INC R0
	JNZ HEX2DEC    ;IF QUOTIENT IS PRESENT THEN DIVIDE AGAIN
	RET

END